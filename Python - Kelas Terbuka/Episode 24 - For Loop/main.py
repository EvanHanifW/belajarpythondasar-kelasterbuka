# LOOPING


## for kondisi :
##     aksi

# for dengan list
angka2 = [0,2,4,8,10]
print(angka2)

for i in angka2:
    print(f'i sekarang -> {i}')
print('Akhir dari program')

# for dengan range
for i in range(1,5):
    print(f'i sekarang -> {i}')
print('Akhir dari program 2')

# for menggunakan string
data_str = 'Saya ganteng banget'

for huruf in data_str:
    print(huruf)
print('Akhir dari program 3')